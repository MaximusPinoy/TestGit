package oopEncapsulation;

public class Person {
	  private String name;
	  private String lastName;
	  
	   // Getter
	   public String getName() {
	     return name;
	   }
	   public String getlastName() {
		     return lastName;
		   }

	   // Setter
	   public void setName(String newName) {
	     this.name = newName;
	   }
	   
	   public void setlastName(String newLastName) {
		   this.lastName = newLastName;
		   }
}
